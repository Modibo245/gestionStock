    private String equiv(String s) {
        if(s=="Ajouer (par défaut)")
            return "select,alter";
        if(s=="Ajouter et modifier")
            return "select,update,alter";
        if(s=="Ajouter, modiffier et supprimer"){
           return "select,update,delete,alter"; 
        }else{
            return "select";
        }
        
    }

